package org.Book;

public class Book {
	private int id;
	private String author;
	private String name;
	private int price;
	public void setId(int id)
	{
		this.id=id;
	}
	public int getId()
	{
		return id;
	}
	public void setAuthor(String A)
	{
		this.author=A;
	}
	public String getAuthor()
	{
		return author;
	}
	public void setName(String n)
	{
		this.name=n;
	}
	public String getName()
	{
		return name;
	}
	public void setPrice(int p)
	{
		this.price=p;
	}
	public int getPrice()
	{
		return price;
	}
	
	

}

